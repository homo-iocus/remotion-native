export const smoothenPoints = (points: Point[]) => {
  return points.map((current, index): Point => {
    const next = points[index + 1];
    if (!next) {
      return current;
    }
    const last = points[index - 1];
    const nextNext = points[index + 2];

    if (!last || !nextNext) {
      return current;
    }

    return {
      lt: (last.lt + current.lt + next.lt + nextNext.lt) / 4,
      ln: (last.ln + current.ln + next.ln + nextNext.ln) / 4,
    };
  });
};

